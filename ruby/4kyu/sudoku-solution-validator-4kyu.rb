def validSolution(board)
        def checkOneRow(row)
                row.sort == (1..9).to_a
        end

        def checkRows(board)
                board.each { |row| return false if !checkOneRow(row) }
                return true
        end

        # rows
        return false if !checkRows(board)
        # cols
        return false if !checkRows(board.transpose)

        # squares
        for n in [0, 3, 6]
                for m in [0, 3, 6]
                        square = []
                        for i in n..(n+2)
                                for j in m..(m+2)
                                        square.push(board[i][j])
                                end
                        end
                        return false if !checkOneRow(square)
                end
        end

        return true
end

